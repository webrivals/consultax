<script>
    const filterInput = document.getElementById("tableFilter");
    filterInput.addEventListener("keyup", function() {
        const filter = this.value.toLowerCase();
        const rows = document.querySelectorAll("table tbody tr");
        rows.forEach(row => {
            const category = row.querySelector(".tbl-col-head").textContent.toLowerCase();
            const details = row.querySelector(".tbl-col-desc").textContent.toLowerCase();
            if (category.includes(filter) || details.includes(filter)) {
                row.style.display = "";
            } else {
                row.style.display = "none";
            }
        });
    });
</script>

