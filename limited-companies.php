                    <h2 class="section-head mt-5">What is a Limited Company?</h2>
                    <p class="section-para">
                        A limited company is a separate legal entity from its owners, offering protection through limited
                        liability. This means shareholders are only responsible for business debts up to the value of their
                        investment, keeping personal assets safe.
                        There are two types of limited companies: Private limited companies (Ltd) and Public limited
                        companies (PLC).
                        Private limited companies are more common for small and medium-sized businesses, while Public
                        limited companies can raise capital by selling shares to the public.
                        Running a limited company can boost your credibility and offers potential tax advantages. However,
                        it also brings responsibilities - including paying Corporation Tax and submitting annual accounts to
                        HMRC. Directors must follow specific rules when withdrawing profits, such as issuing dividends or
                        using director’s loans.
                        This structure provides a clear, professional framework for managing your business finances
                        effectively and responsibly.
